function clusterentropy(clusters, types)
	tys = unique(types)
	cls = unique(clusters)

	vec = []
	cnt = []
	for c in cls
		ent = 0
		push!(cnt, sum(clusters .== c))
		for t in tys
			# tmp = sum((types[clusters .== c] .== t))/sum(clusters .== c ) 
			tmp = sum((types .== t) .& (clusters .== c))/sum(clusters .== c ) 
			ent += - tmp * log(tmp)
		end
		push!(vec, ent)
	end
	return sum(vec, Weights(cnt ./ sum(cnt)))
end

