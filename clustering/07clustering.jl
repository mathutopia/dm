md"""
## 2 聚类效果评价
好的聚类方法产生高质量的簇，即簇内的对象具有高的相似度和不同簇之间具有低的相似度。由于存在大量不同类型的聚类算法，而每种聚类算法可能都定义了自己的簇类型，每种情况都可能需要一种不同的评估度量。
传统的用于评估簇的度量有两类：监督的度量和非监督的度量。 由于非监督度量一般会跟具体算法有关， 我们这里仅展示几个监督度量评价标准。

监督度量也称外部质量评价准则， 是基于一个已经存在的人工分类数据集(已知每个对象的类别)进行评价的，这样可以将聚类输出结果直接与之进行比较。外部质量评价准则与聚类算法无关，理想的聚类结果是具有相同类别的数据被聚集到相同的簇中，具有不同类别的数据聚集在不同的簇中。

### a) 准确度等
最常见的准则是准确度等指标。为了计算准确度等一类的指标， 我们可以将样本原先所在的每一个类别看成一个单独的簇， 将其作为baseline， 与样本被聚类之后对应的簇去对比计算。 理想的状况是一个类别对应一个簇。 

实现这一功能的函数是`counts函数`， 其基本的用法是：`counts(a::ClusteringResult, b::ClusteringResult) -> Matrix{Int}`， 计算的是同一个数据集的两个聚类结果中，相互重叠的样本量。即， counts这里返回的是一个$n_a \times n_b$矩阵, 其中$n_a 、 n_b$分别表示两个聚类结果包含的类别数， 结果矩阵的i,j元素C[i,j]表示在聚类结果a,b中，a的第i个类别与b的第j个类别的交集中的样本数。 除此以外， 该函数还允许另一个参数是一个向量。即：
```
counts(a::ClusteringResult, b::ClusteringResult) -> Matrix{Int}
counts(a::ClusteringResult, b::AbstractVector{<:Integer}) -> Matrix{Int}
counts(a::AbstractVector{<:Integer}, b::ClusteringResult) -> Matrix{Int}
```

这样， 我们需要分别求出样本原先所在的类别和聚类之后的簇编号。 由于原始数据中类别是CategoricalVector类型， 且元素值是文本， 我们可以先将文本转换为类别标签（需要是整数），再使用函数`unwrap函数`将其转换为纯粹的向量。本质上， unwrap函数是将CategoricalArrays.CategoricalVector这层包裹去掉， 得到一个纯粹的向量。


